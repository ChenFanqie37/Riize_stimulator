export function getTriggeredContent(trigger: string, gameState: any): any | null {
  const pool = triggeredContentPool[trigger]
  if (!pool || pool.length === 0) return null
  const idx = Math.floor(Math.random() * pool.length)
  const item = pool[idx]
  return {
    platform: item.platform,
    content: item.build(gameState),
  }
}
